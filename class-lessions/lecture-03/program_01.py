# Question: Write a program to accept two integers and check whether they are equal or
# not.

# Collect two integer numbers from user.
number_01 = int(input("Enter the first number: "))
number_02 = int(input("Enter the second number: "))

# Compare received two numbers, they are equal or not.
if number_01 == number_02:
    print("Entered two number is equal.")
else:
    print("Entered two number is not equal.")
